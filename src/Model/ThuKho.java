package Model;

public class ThuKho {

    private int id;
    private String ten_thukho;
    private int ma_kho;

    public int getID() {
        return id;
    }

    public void setID(int id) {
        this.id = id;
    }

    public String getThuKho() {
        return ten_thukho;
    }

    public void setThuKho(String thukho) {
        this.ten_thukho = thukho;
    }

    public int getMaKho() {
        return ma_kho;
    }

    public void setMaKho(int kho) {
        this.ma_kho = kho;
    }
}
